package com.kaanyagan.omubumu.models;

public class SubCategoryModel {
    private String subCategoryId, subCategoryName, subCategoryImg, subCategoryQuestionCount;

    public SubCategoryModel() {
        this.subCategoryId = subCategoryId;
        this.subCategoryName = subCategoryName;
        this.subCategoryImg = subCategoryImg;
        this.subCategoryQuestionCount = subCategoryQuestionCount;
    }

    public String getSubCategoryId() {
        return subCategoryId;
    }

    public void setSubCategoryId(String subCategoryId) {
        this.subCategoryId = subCategoryId;
    }

    public String getSubCategoryName() {
        return subCategoryName;
    }

    public void setSubCategoryName(String subCategoryName) {
        this.subCategoryName = subCategoryName;
    }

    public String getSubCategoryImg() {
        return subCategoryImg;
    }

    public void setSubCategoryImg(String subCategoryImg) {
        this.subCategoryImg = subCategoryImg;
    }

    public String getSubCategoryQuestionCount() {
        return subCategoryQuestionCount;
    }

    public void setSubCategoryQuestionCount(String subCategoryQuestionCount) {
        this.subCategoryQuestionCount = subCategoryQuestionCount;
    }
}
