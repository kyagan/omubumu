package com.kaanyagan.omubumu.models;

public class MainCategoryModel {
    private String mainCategoryId,mainCategoryName,mainCategoryImg;

    public MainCategoryModel() {
        this.mainCategoryId = mainCategoryId;
        this.mainCategoryName = mainCategoryName;
        this.mainCategoryImg = mainCategoryImg;
    }

    public String getMainCategoryId() {
        return mainCategoryId;
    }

    public void setMainCategoryId(String mainCategoryId) {
        this.mainCategoryId = mainCategoryId;
    }

    public String getMainCategoryName() {
        return mainCategoryName;
    }

    public void setMainCategoryName(String mainCategoryName) {
        this.mainCategoryName = mainCategoryName;
    }

    public String getMainCategoryImg() {
        return mainCategoryImg;
    }

    public void setMainCategoryImg(String mainCategoryImg) {
        this.mainCategoryImg = mainCategoryImg;
    }
}
